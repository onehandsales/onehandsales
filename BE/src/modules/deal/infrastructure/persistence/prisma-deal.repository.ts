  async listDeals(input: ListDealsInput): Promise<DealListResult> {
    const where = this.createDealWhere(input);
    const [deals, totalCount, stageSummary] = await Promise.all([
      this.prismaService.deal.findMany({
        where,
        include: {
          company: { select: { name: true } },
          contact: { select: { name: true } },
        },
        orderBy: { updatedAt: "desc" },
        skip: (input.page - 1) * input.pageSize,
        take: input.pageSize,
      }),
      this.prismaService.deal.count({ where }),
      this.getStageSummary(input.userId),
    ]);

    return {
      items: await Promise.all(
        deals.map((deal) => this.mapDealRecord(deal))
      ),
      stageSummary,
      page: input.page,
      pageSize: input.pageSize,
      totalCount,
      hasNext: input.page * input.pageSize < totalCount,
    };
  }

  async createDeal(input: CreateDealInput): Promise<DealRecord> {
    return this.prismaService.$transaction(async (transaction) => {
      const relations = await this.resolveRelations(transaction, input.userId, {
        companyId: input.companyId,
        contactId: input.contactId,
      });
      await this.assertProducts(transaction, input.userId, input.productIds);
      const deal = await transaction.deal.create({
        data: {
          userId: input.userId,
          companyId: relations.companyId,
          contactId: relations.contactId,
          title: input.title,
          amount: input.amount,
          currency: input.currency,
          stage: input.stage,
          likelihoodStatus: input.likelihoodStatus,
          likelihoodPercent: input.likelihoodPercent,
          expectedCloseDate: input.expectedCloseDate,
          nextActionTitle: input.nextActionText,
          nextActionDueAt: input.nextActionDueAt,
          nextActionStatus: this.resolveInitialNextActionStatus(
            input.nextActionText,
            input.nextActionDueAt
          ),
        },
        include: {
          company: { select: { name: true } },
          contact: { select: { name: true } },
        },
      });

      await this.replaceProductConnections(transaction, {
        userId: input.userId,
        dealId: deal.id,
        productIds: input.productIds,
      });

      if (input.initialMemo) {
        const encrypted = await this.encryptionPort.encryptText(input.initialMemo);
        await transaction.personalMemo.create({
          data: {
            userId: input.userId,
            targetType: PersonalMemoTargetType.DEAL,
            targetId: deal.id,
            title: "초기 Memo",
            contentCiphertext: encrypted.ciphertext,
            contentKeyVersion: encrypted.keyVersion,
          },
        });
      }

      await this.createAutoActivity(transaction, {
        userId: input.userId,
        dealId: deal.id,
        typeName: DEFAULT_ACTIVITY_TYPE_NAME,
        occurredAt: deal.createdAt,
        title: "딜 생성",
        content: null,
      });

      return this.mapDealRecord(deal, transaction);
    });
  }

  async getDealDetail(
    userId: string,
    dealId: string
  ): Promise<DealDetailRecord | null> {
    const deal = await this.prismaService.deal.findFirst({
      where: { id: dealId, userId },
      include: {
        company: { select: { name: true } },
        contact: { select: { name: true } },
      },
    });

    if (!deal) {
      return null;
    }

    const [products, activities, memos, schedulesSummary, meetingNotesSummary] =
      await Promise.all([
        this.listDealProducts(userId, dealId),
        this.listRecentActivities(userId, dealId),
        this.listMemos(userId, dealId),
        this.getSchedulesSummary(userId, dealId),
        this.getMeetingNotesSummary(userId, dealId),
      ]);

    return {
      deal: await this.mapDealRecord(deal),
      products,
      activities,
      memos,
      schedulesSummary,
      meetingNotesSummary,
    };
  }

  async updateDeal(input: UpdateDealInput): Promise<DealRecord> {
    return this.prismaService.$transaction(async (transaction) => {
      const existing = await this.assertDealExists(
        input.userId,
        input.dealId,
        "write",
        transaction
      );
      const relations =
        input.companyId !== undefined || input.contactId !== undefined
          ? await this.resolveRelations(transaction, input.userId, {
              companyId:
                input.companyId !== undefined
                  ? input.companyId
                  : existing.companyId,
              contactId:
                input.contactId !== undefined
                  ? input.contactId
                  : existing.contactId,
            })
          : null;

      if (input.productIds !== undefined) {
        await this.assertProducts(transaction, input.userId, input.productIds);
      }

      const data: Prisma.DealUpdateInput = {};

      if (input.title !== undefined) {
        data.title = input.title;
      }

      if (
        relations &&
        (input.companyId !== undefined || input.contactId !== undefined)
      ) {
        data.company = relations?.companyId
          ? { connect: { id: relations.companyId } }
          : { disconnect: true };
      }

      if (input.contactId !== undefined) {
        data.contact = relations?.contactId
          ? { connect: { id: relations.contactId } }
          : { disconnect: true };
      }

      if (input.amount !== undefined) {
        data.amount = input.amount;
      }

      if (input.currency !== undefined) {
        data.currency = input.currency;
      }

      if (input.stage !== undefined) {
        data.stage = input.stage;
      }

      if (input.likelihoodStatus !== undefined) {
        data.likelihoodStatus = input.likelihoodStatus;
      }

      if (input.likelihoodPercent !== undefined) {
        data.likelihoodPercent = input.likelihoodPercent;
      }

      if (input.expectedCloseDate !== undefined) {
        data.expectedCloseDate = input.expectedCloseDate;
      }

      if (input.nextActionText !== undefined) {
        data.nextActionTitle = input.nextActionText;
      }

      if (input.nextActionDueAt !== undefined) {
        data.nextActionDueAt = input.nextActionDueAt;
      }

      if (input.nextActionStatus !== undefined) {
        data.nextActionStatus = input.nextActionStatus;
      } else if (
        input.nextActionText !== undefined ||
        input.nextActionDueAt !== undefined
      ) {
        data.nextActionStatus = this.resolveInitialNextActionStatus(
          input.nextActionText !== undefined
            ? input.nextActionText
            : existing.nextActionTitle,
          input.nextActionDueAt !== undefined
            ? input.nextActionDueAt
            : existing.nextActionDueAt
        );
      }

      const deal = await transaction.deal.update({
        where: { id: input.dealId },
        data,
        include: {
          company: { select: { name: true } },
          contact: { select: { name: true } },
        },
      });

      if (input.productIds !== undefined) {
        await this.replaceProductConnections(transaction, {
          userId: input.userId,
          dealId: deal.id,
          productIds: input.productIds,
        });
      }

      return this.mapDealRecord(deal, transaction);
    });
  }

  async changeDealStage(input: ChangeDealStageInput): Promise<DealRecord> {
    return this.prismaService.$transaction(async (transaction) => {
      const existing = await this.assertDealExists(
        input.userId,
        input.dealId,
        "write",
        transaction
      );

      if (existing.stage === input.stage) {
        return this.mapDealRecord(existing, transaction);
      }

      const deal = await transaction.deal.update({
        where: { id: input.dealId },
        data: { stage: input.stage },
        include: {
          company: { select: { name: true } },
          contact: { select: { name: true } },
        },
      });

      await this.createAutoActivity(transaction, {
        userId: input.userId,
        dealId: input.dealId,
        typeName: STAGE_ACTIVITY_TYPE_NAME,
        occurredAt: input.occurredAt,
        title:
          input.activityTitle ??
          `단계 변경: ${this.toStageLabel(existing.stage)} -> ${this.toStageLabel(input.stage)}`,
        content: input.activityContent,
      });

      return this.mapDealRecord(deal, transaction);
    });
  }

