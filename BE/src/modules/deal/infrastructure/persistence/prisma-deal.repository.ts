  private createDealWhere(input: ListDealsInput): Prisma.DealWhereInput {
    return {
      userId: input.userId,
      ...(input.stage ? { stage: input.stage } : {}),
      ...(input.likelihoodStatus
        ? { likelihoodStatus: input.likelihoodStatus }
        : {}),
      ...(input.companyId ? { companyId: input.companyId } : {}),
      ...(input.contactId ? { contactId: input.contactId } : {}),
      ...(input.nextActionStatus
        ? { nextActionStatus: input.nextActionStatus }
        : {}),
      ...(input.includeDeleted ? {} : { deletedAt: null }),
      ...(input.search
        ? {
            OR: [
              { title: { contains: input.search, mode: "insensitive" } },
              {
                company: {
                  name: { contains: input.search, mode: "insensitive" },
                },
              },
              {
                contact: {
                  name: { contains: input.search, mode: "insensitive" },
                },
              },
            ],
          }
        : {}),
    };
  }

  private async resolveRelations(
    client: DealPrismaClient,
    userId: string,
    input: {
      readonly companyId: string | null;
      readonly contactId: string | null;
    }
  ): Promise<ResolvedRelations> {
    const company = input.companyId
      ? await this.assertCompany(client, userId, input.companyId)
      : null;
    const contact = input.contactId
      ? await this.assertContact(client, userId, input.contactId)
      : null;
    const inferredCompanyId = company?.id ?? contact?.companyId ?? null;

    if (company && contact?.companyId && contact.companyId !== company.id) {
      throw new ValidationDomainError("Contact does not belong to the company");
    }

    if (inferredCompanyId && !company) {
      await this.assertCompany(client, userId, inferredCompanyId);
    }

    return {
      companyId: inferredCompanyId,
      contactId: contact?.id ?? null,
    };
  }

  private async assertCompany(
    client: DealPrismaClient,
    userId: string,
    companyId: string
  ): Promise<CompanyRecord> {
    const company = await client.company.findUnique({ where: { id: companyId } });

    if (!company) {
      throw new RelatedEntityNotFoundError();
    }

    if (company.userId !== userId) {
      throw new OwnershipViolationError();
    }

    if (company.deletedAt) {
      throw new DeletedResourceError("write");
    }

    return company;
  }

  private async assertContact(
    client: DealPrismaClient,
    userId: string,
    contactId: string
  ): Promise<ContactRecord> {
    const contact = await client.contact.findUnique({ where: { id: contactId } });

    if (!contact) {
      throw new RelatedEntityNotFoundError();
    }

    if (contact.userId !== userId) {
      throw new OwnershipViolationError();
    }

    if (contact.deletedAt) {
      throw new DeletedResourceError("write");
    }

    return contact;
  }

  private async assertProducts(
    client: DealPrismaClient,
    userId: string,
    productIds: readonly string[]
  ): Promise<void> {
    for (const productId of productIds) {
      const product = await client.product.findUnique({ where: { id: productId } });

      if (!product) {
        throw new RelatedEntityNotFoundError();
      }

      if (product.userId !== userId) {
        throw new OwnershipViolationError();
      }

      if (product.deletedAt) {
        throw new DeletedResourceError("write");
      }
    }
  }

  private async replaceProductConnections(
    client: DealPrismaClient,
    input: {
      readonly userId: string;
      readonly dealId: string;
      readonly productIds: readonly string[];
    }
  ): Promise<void> {
    const activeConnections = await client.productConnection.findMany({
      where: {
        userId: input.userId,
        targetType: ProductConnectionTargetType.DEAL,
        targetId: input.dealId,
        deletedAt: null,
      },
    });
    const nextProductIds = new Set(input.productIds);
    const now = new Date();
    const permanentDeleteAt = new Date(now.getTime() + 30 * 24 * 60 * 60 * 1000);

    for (const connection of activeConnections) {
      if (!nextProductIds.has(connection.productId)) {
        await client.productConnection.update({
          where: { id: connection.id },
          data: { deletedAt: now, permanentDeleteAt },
        });
      }
    }

    const existingProductIds = new Set(
      activeConnections
        .filter((connection) => nextProductIds.has(connection.productId))
        .map((connection) => connection.productId)
    );

    for (const productId of input.productIds) {
      if (!existingProductIds.has(productId)) {
        await client.productConnection.create({
          data: {
            userId: input.userId,
            productId,
            targetType: ProductConnectionTargetType.DEAL,
            targetId: input.dealId,
            connectionType: ProductConnectionType.PROPOSED,
          },
        });
      }
    }
  }

  private async assertDealExists(
    userId: string,
    dealId: string,
    operation: "read" | "write",
    client: DealPrismaClient = this.prismaService
  ): Promise<DealRow> {
    const deal = await client.deal.findFirst({
      where: { id: dealId, userId },
      include: {
        company: { select: { name: true } },
        contact: { select: { name: true } },
      },
    });

    if (!deal) {
      throw new DealNotFoundError();
    }

    if (deal.deletedAt) {
      throw new DeletedResourceError(operation);
    }

    return deal;
  }

  private async assertActivityType(
    userId: string,
    typeId: string,
    client: DealPrismaClient = this.prismaService
  ): Promise<void> {
    const type = await client.dealActivityType.findUnique({ where: { id: typeId } });

    if (!type || (type.userId !== null && type.userId !== userId)) {
      throw new DealActivityTypeNotFoundError();
    }
  }

  private async createAutoActivity(
    client: DealPrismaClient,
    input: {
      readonly userId: string;
      readonly dealId: string;
      readonly typeName: string;
      readonly occurredAt: Date;
      readonly title: string;
      readonly content: string | null;
    }
  ): Promise<void> {
    const typeId = await this.findOrCreateSystemActivityType(
      client,
      input.typeName
    );
    await client.dealActivity.create({
      data: {
        userId: input.userId,
        dealId: input.dealId,
        typeId,
        activityDate: input.occurredAt,
        title: input.title,
        content: input.content,
        isAutoGenerated: true,
      },
    });
  }

  private async findOrCreateSystemActivityType(
    client: DealPrismaClient,
    name: string
  ): Promise<string> {
    const existing = await client.dealActivityType.findFirst({
      where: { userId: null, name },
      orderBy: { createdAt: "asc" },
    });

    if (existing) {
      return existing.id;
    }

    const type = await client.dealActivityType.create({
      data: { name, isSystem: true },
    });

    return type.id;
  }

  private assertNextActionExists(deal: DealRow): void {
    if (!deal.nextActionTitle && !deal.nextActionDueAt) {
      throw new NextActionNotFoundError();
    }
  }

  private async mapDealRecord(
    deal: DealRow,
    client: DealPrismaClient = this.prismaService
  ): Promise<DealRecord> {
    return {
      id: deal.id,
      userId: deal.userId,
      title: deal.title,
      companyId: deal.companyId,
      companyName: deal.company?.name ?? null,
      contactId: deal.contactId,
      contactName: deal.contact?.name ?? null,
      amount: Number(deal.amount),
      currency: deal.currency,
      stage: deal.stage,
      likelihoodStatus: deal.likelihoodStatus,
      likelihoodPercent: deal.likelihoodPercent,
      expectedCloseDate: deal.expectedCloseDate,
      nextActionText: deal.nextActionTitle,
      nextActionDueAt: deal.nextActionDueAt,
      nextActionStatus: deal.nextActionStatus,
      memoSummary: await this.getMemoSummary(client, deal.userId, deal.id),
      createdAt: deal.createdAt,
      updatedAt: deal.updatedAt,
      deletedAt: deal.deletedAt,
      permanentDeleteAt: deal.permanentDeleteAt,
    };
  }

