  async deleteDeal(
    userId: string,
    dealId: string,
    now: Date,
    permanentDeleteAt: Date
  ): Promise<DeleteResultRecord> {
    const deal = await this.prismaService.deal.findFirst({
      where: { id: dealId, userId },
    });

    if (!deal) {
      throw new DealNotFoundError();
    }

    if (deal.deletedAt) {
      throw new DeletedResourceError("write");
    }

    await this.prismaService.deal.update({
      where: { id: dealId },
      data: { deletedAt: now, permanentDeleteAt },
    });

    return { id: dealId, deletedAt: now, permanentDeleteAt };
  }

  async restoreDeal(userId: string, dealId: string): Promise<DealRecord> {
    const deal = await this.prismaService.deal.findFirst({
      where: { id: dealId, userId },
    });

    if (!deal) {
      throw new DealNotFoundError();
    }

    const restored = await this.prismaService.deal.update({
      where: { id: dealId },
      data: { deletedAt: null, permanentDeleteAt: null },
      include: {
        company: { select: { name: true } },
        contact: { select: { name: true } },
      },
    });

    return this.mapDealRecord(restored);
  }

